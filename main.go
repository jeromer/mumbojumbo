package main

import (
	"bytes"
	"flag"
	"fmt"
	"math/rand"
	"os"
	"strings"
	"text/template"
	"time"
	"unsafe"
)

const (
	VERSION = "1.1"
)

var (
	s  = flag.String("s", "", "string to obfuscate")
	sf = flag.String("sf", "", "string file to obfuscate")
	p  = flag.String("p", "", "pkg name for the generated code")
)

var tpl = `// CODE GENERATED BY mumbojumbo {{ .Version }} (https://github.com/jeromer/mumbojumbo) DO NOT EDIT !!!!

package {{ .Pkg }}

import (
	"unsafe"
)

func eax() uint8{
	return uint8(unsafe.Sizeof(true))
}

func Get() string {
{{ .Obfuscated }}
}
`

func main() {
	flag.Parse()

	var text string

	if *s == "" {
		if *sf != "" {
			b, err := os.ReadFile(*sf)
			if err != nil {
				quit(err.Error())
			}
			text = string(b)
		} else {
			quit("no string provided")
		}
	} else {
		text = *s
	}

	if *p == "" {
		quit("no pkg provided")
	}

	fmt.Println(
		generateGoCode(
			tplData{
				Version:    VERSION,
				Pkg:        *p,
				Obfuscated: obfuscate(text),
			},
		),
	)
}

// ----

type tplData struct {
	Version    string
	Pkg        string
	Obfuscated string
}

func generateGoCode(data tplData) string {
	tmpl := template.Must(
		template.New("x").Parse(tpl),
	)

	w := new(bytes.Buffer)
	err := tmpl.Execute(w, data)
	if err != nil {
		panic(err)
	}

	return w.String()
}

